package week1.date2;

public class Operators {

	public static void main(String[] args) {

		int a = 9;
		int b = 3;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a / b);
		System.out.println(a % b);
		System.out.println(a * b);

		System.out.println(a > b && a < b);
		System.out.println(a > b || a < b);

		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);

	}
}